#include <unistd.h>

void	ft_putstr(char *str)
{
	char *my_str;

	my_str = str;
	while (*my_str != 0)
	{
		write(1, my_str, 1);
		my_str++;
	}
	write(1, "\n", 1);
}

int		main(int argc, char *argv[])
{
	int i;

	i = 1;
	while (i < argc)
	{
		ft_putstr(argv[i]);
		i++;
	}
	return (0);
}

