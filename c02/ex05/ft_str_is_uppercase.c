int	ft_str_is_uppercase(char *str)
{
	char *my_char;

	my_char = str;
	while (*my_char != 0)
	{
		if (*my_char < 'A' || *my_char > 'Z')
		{
			return (0);
		}
		my_char++;
	}
	return (1);
}
